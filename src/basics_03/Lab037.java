package src.basics_03;

public class Lab037 {
    public static void main(String[] args) {
        //Ternary Operator
        //MAX and MIN of two numbers

        int a = 10;
        int b = 20;
        int max = a<b ? b : a;
        System.out.println(max);

        int min = a<b ? a : b;
        System.out.println(min);

    }
}
